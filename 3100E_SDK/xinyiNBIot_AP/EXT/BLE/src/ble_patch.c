int ble_patch_init(void)
{
	uint32_t offset = 0;
	uint32_t patch_size;
	int ble_state = BLE_FALSE;
	uint8_t *ble_patch = NULL;
	uint32_t tickstart = 0;


	if(g_ble_rfparam == 100)
    {
        patch_size = ble_patch_def[0] + (ble_patch_def[1] << 8);
        ble_patch = xy_malloc(patch_size);
        memcpy(ble_patch, ble_patch_def, patch_size);
    }
    else//下载连接仪表测试的patch
    {
        patch_size = ble_patch_test[0] + (ble_patch_test[1] << 8);
        ble_patch = xy_malloc(patch_size);
        memcpy(ble_patch, ble_patch_test, patch_size);

		if(g_ble_rfpower == 0)
		{
			ble_patch[BLE_RF_POWER_SITE1] = 0x6c;
			ble_patch[BLE_RF_POWER_SITE2] = 0x07;
			ble_patch[BLE_RF_POWER_SITE3] = 0xa3;
			ble_patch[BLE_RF_POWER_SITE4] = 0x07;

			ble_patch[BLE_RF_FREQOFFSET_SITE1] = g_ble_rfparam;
			ble_patch[BLE_RF_CHECK_SITE2] = g_ble_rfparam+0xB7;
		}
		else
		{
			ble_patch[BLE_RF_POWER_SITE1] = 0xf0;
			ble_patch[BLE_RF_POWER_SITE2] = 0x0f;
			ble_patch[BLE_RF_POWER_SITE3] = 0xaa;
			ble_patch[BLE_RF_POWER_SITE4] = 0x0a;

			ble_patch[BLE_RF_FREQOFFSET_SITE1] = g_ble_rfparam;
			
			ble_patch[BLE_RF_CHECK_SITE2] = g_ble_rfparam+0x4d;
		}
	}

	offset = BLE_PATCH_TOTAL_LEN_OFFSET;
	ble_msg_node_t *msg_node = NULL;
	// send blocks.
	while (offset < patch_size)
	{
		tickstart = Get_Tick();
		ble_state = BLE_FALSE;
		// Send block data;
		ble_uart_write((uint8_t *)(ble_patch + offset + BLE_PATCH_BLOCK_LEN_OFFSET), ble_patch[offset]);
		offset += (BLE_PATCH_BLOCK_LEN_OFFSET + ble_patch[offset]);
		//等待ble_uart_write触发的下载patch的回复
		while (GetListNum(&g_ble_msg_head) == 0 && !Check_Ms_Timeout(tickstart,BLE_WAIT_CMP_MS));

		while ((msg_node = (ble_msg_node_t *)ListRemove(&g_ble_msg_head)) != NULL)
		{
			if(ble_state == BLE_TRUE)
			{
				xy_assert(0);//最后一包patch的回复和ready的上报连一起
			}
			if ((0x04 != msg_node->data[0]) || (0x0e != msg_node->data[1]) || (BLE_CMD_OGF != (uint8_t)(msg_node->data[5])) || (0x00 != msg_node->data[6]))
			{
				ble_state = BLE_FALSE;
				xy_free(ble_patch);
				return ble_state;
			}
			else
			{
				ble_state = BLE_TRUE;
			}
			xy_free(msg_node);
			msg_node = NULL;
		}
	}

	xy_free(ble_patch);
	return ble_state;
}

#define BLE_NVRAM_LENGTH			(170)

void save_ble_nvram(void *payload)
{
	xy_ftl_write(CALIB_FREQ_BASE+sizeof(Cali_Ftl_t)+FLASH_AP_TIME_LEN,payload,BLE_NVRAM_LENGTH);
}


BLE_ERR_E restore_nvram_to_ble()
{
	BLE_ERR_E ret = BLE_OK;
	void *ble_NV_ram = xy_malloc(BLE_NVRAM_LENGTH);
	
	if(xy_ftl_read(CALIB_FREQ_BASE+sizeof(Cali_Ftl_t)+FLASH_AP_TIME_LEN,ble_NV_ram,BLE_NVRAM_LENGTH) == 1)
		ret = ble_config_op(HCI_CMD_SET_NVRAM, ble_NV_ram, BLE_NVRAM_LENGTH);

	xy_free(ble_NV_ram);
	return ret;
}

